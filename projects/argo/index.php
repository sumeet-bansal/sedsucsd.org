							<h1 class="font-condensed align-left mt-10 mb-20 mb-sm-20">The Competition</h1>
							<i>
							For the past half century, the only artificial objects orbiting Earth have been manned spacecraft, space stations and satellites. As we begin to open up space to new opportunities, from new orbits to new applications, both commercial and government organizations are exploring the creation of a new orbital vehicle. It would be a multi-functional workhorse that, with minimal retrofitting, could tackle assignments from relocating objects in orbit to cleaning up space debris, servicing space stations and even shuttling between Earth and Lunar orbit. It would be, in a word, a space "tug," like the tugboats that are the workhorses of seaports around the planet.
							<br><br>
							Your assignment is to envision a multi-purpose space tug designed to operate in Earth orbit and cislunar space by defining its service requirements, markets to be served, and technical capabilities, together with an estimation of deployment and operating costs.
							</i><br><br>
							Argo is a space-tug designed for multiple trips between LEO and lunar orbit while carrying an upwards of 34 metric tons of cargo. It is also designed to be capable of delivering satellites to GEO or lunar orbits. The structure, electronics, and data acquisition systems must support the load and provide control over a multiple mission lifespan. The estimated mission time is anywhere from 45 days to 95 days, dependent on the mass of the cargo Argo is transporting. The ideal lifespan from launch is 30 clients in 7 years of continuous operation. Argo will be retrofitted if future technology facilitates either full recovery of the vehicle and/or continuous maintenance. When the operational lifespan of Argo has been exceeded, and reliability can no longer be guaranteed to our potential contracts, the craft will be transitioned to a heliocentric graveyard orbit. This ensures the craft does not orbitally decay into Earthâ€™s upper atmosphere, which would cause a disassembly of the craft and its reactors, and a subsequent spread of radioactive material across the Earth.
